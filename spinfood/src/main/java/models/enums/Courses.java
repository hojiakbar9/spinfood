package models.enums;

/**
 * The Courses enum represents the different courses in a meal for the Spinfood event.
 * It includes APPETIZER, MAIN, and DESSERT as the possible courses.
 */
public enum Courses {
    /**
     * Represents the appetizer course in a meal.
     */
    APPETIZER,

    /**
     * Represents the main course in a meal.
     */
    MAIN,

    /**
     * Represents the dessert course in a meal.
     */
    DESSERT
}
